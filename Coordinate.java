public class Coordinate {
    int x;
    int y;
    char e;

    public Coordinate(int x,int y,char e) {
        this.x = x;
        this.y = y;
        this.e = e;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public char getE() {
        return e;
    }

    public void setE(char e) {
        this.e = e;
    }

    
}